-- ═══════════════════════════════════════════════════════════════════
-- Point-in-polygon (ray casting algorithm)
-- ═══════════════════════════════════════════════════════════════════

--- @param px number
--- @param py number
--- @param poly table array of {x, y}
--- @return boolean
function TCGZones.PointInPolygon(px, py, poly)
    local n = #poly
    local inside = false
    local j = n
    for i = 1, n do
        local xi, yi = poly[i].x, poly[i].y
        local xj, yj = poly[j].x, poly[j].y
        if ((yi > py) ~= (yj > py)) and (px < (xj - xi) * (py - yi) / (yj - yi) + xi) then
            inside = not inside
        end
        j = i
    end
    return inside
end

-- ═══════════════════════════════════════════════════════════════════
-- Distance from point to polygon edge (for nearest-zone fallback)
-- ═══════════════════════════════════════════════════════════════════

--- @param px number
--- @param py number
--- @param poly table array of {x, y}
--- @return number
function TCGZones.DistanceToPolygon(px, py, poly)
    local minDist = math.huge
    local n = #poly
    local j = n
    for i = 1, n do
        local x1, y1 = poly[i].x, poly[i].y
        local x2, y2 = poly[j].x, poly[j].y
        local dx, dy = x2 - x1, y2 - y1
        local lenSq = dx * dx + dy * dy
        local t = lenSq == 0 and 0 or math.max(0, math.min(1, ((px - x1) * dx + (py - y1) * dy) / lenSq))
        local projX = x1 + t * dx
        local projY = y1 + t * dy
        local dist = math.sqrt((px - projX)^2 + (py - projY)^2)
        if dist < minDist then minDist = dist end
        j = i
    end
    return minDist
end

-- ═══════════════════════════════════════════════════════════════════
-- Get zone name for a GTA position
-- Fallback 1: nearest polygon (max 2000m)
-- Fallback 2: quadrant (center cross: x=-627, y=2558)
-- ═══════════════════════════════════════════════════════════════════

--- @param x number
--- @param y number
--- @return string
function TCGZones.GetZoneName(x, y)
    -- 1. Exact polygon match
    for _, zone in ipairs(TCGZones.Polygons) do
        if TCGZones.PointInPolygon(x, y, zone.points) then
            return zone.name
        end
    end

    -- 2. Nearest polygon (fills gaps between adjacent zones)
    local nearestZone = ''
    local nearestDist = math.huge
    for _, zone in ipairs(TCGZones.Polygons) do
        local dist = TCGZones.DistanceToPolygon(x, y, zone.points)
        if dist < nearestDist then
            nearestDist = dist
            nearestZone = zone.name
        end
    end
    if nearestZone ~= '' and nearestDist < 2000 then
        return nearestZone
    end

    -- 3. Quadrant fallbacks (cross center: x=-627, y=2558)
    local centerX, centerY = -627, 2558
    if y > centerY then
        return x < centerX and 'North West' or 'North East'
    else
        return x < centerX and 'South West' or 'South East'
    end
end
